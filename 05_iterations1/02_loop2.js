// Video 28: while and do while loop

//************************ while loop
let index=0
while(index<=10){
    //console.log(`Value of index is: ${index}`)
    index=index+2
}

//************************ while loop: array

let myArr = ['John','Monty','Rohan']
let arr=0
while(arr<myArr.length){
    //console.log(`Value is: ${myArr[arr]}`)
    arr = arr+1
}

//****************** do while
let i=0
do {
    console.log(i);
    i++;
} while (i<=10);
